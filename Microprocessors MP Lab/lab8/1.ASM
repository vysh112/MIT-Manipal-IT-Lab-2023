DATA SEGMENT
MSG DB 'enter A CHARACTER- $'
MSG2 DB 'YOU ENTERED- $'
DATA ENDS

CODE SEGMENT
ASSUME CS:CODE,DS:DATA

START: MOV AX,DATA
MOV DS,AX

MOV DX, OFFSET MSG
MOV AH, 09H
INT 21H

MOV AH,08H; CHAR INPUT WITHOUT ECHO
INT 21H ; BY DEFAULT INPUT STORED IN AL

MOV BH,AL

LEA DX,MSG2
MOV AH,09H
INT 21H

MOV DL,BH; COUT<<BH
MOV AH,02H
INT 21H

MOV DX, OFFSET MSG
MOV AH,09H
INT 21H

MOV AH, 01H; CHAR INPUT WITH ECHO
INT 21H

MOV BH,AL

LEA DX,MSG2
MOV AH,09H
INT 21H

MOV DL,BH
MOV AH,02H
INT 21H

MOV AH,4CH
INT 21H
CODE ENDS
END START
